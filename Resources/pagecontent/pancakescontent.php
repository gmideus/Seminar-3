    <h2>
        Pancakes
    </h2>
    <img class="recipe" src="Resources/images/pancakes.jpg" alt="Pancakes"/>
    <h3>
        Ingredients
    </h3>
    <ul class="ingredients">
        <li>1 1/2 cups flour</li>
        <li>3 1/2 teaspoons baking powder</li>
        <li>1 1/4 cups milk</li>
        <li>1 egg</li>
        <li>3 tablespoons butter</li>
        <li>1 teaspoon salt</li>
        <li>1 tablespoon white sugar</li>
    </ul>
    <h3>
        Instructions
    </h3>

    <p>
        Mix flour, baking powder, sugar and salt in a bowl. Add the egg, milk and melted butter and mix until smooth.
        Pour the batter into a pan and fry on medium heat for a few minutes.
    </p>

    <p>
        Serve with maple syrup.
    </p>

