    <h2>
        Meatballs
    </h2>
    <img class="recipe" src="Resources/images/meatballs.jpg" alt="Meatballs"/>
    <h3>
        Ingredients
    </h3>
    <ul class="ingredients">
        <li>500g Ground beef</li>
        <li>Salt</li>
        <li>Black pepper</li>
    </ul>
    <h3>
        Instructions
    </h3>

    <p>
        Start by mixing the ground beef, salt and pepper in a bowl and then, with your hands, shape the it into balls about
        an inch and a half in diameter. Heat a pan and fry the meatballs on medium heat for about 10 minutes or until they get
        a dark brown color.
    </p>

    <p>
        Serve with mashed potatoes and gravy.
    </p>


